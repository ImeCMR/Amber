//
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
// The first layer kernel for mlses NN
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
__global__
void first_layer(int nbatch, float *a0, float *a1, float *z1) {

   int batch = (blockIdx.x * blockDim.x + threadIdx.x);

   if ( batch < nbatch) {
      for (int neuron = 0; neuron < nneuron1; neuron++) {
         float weight = 0.0;
         for (int link = 0; link < nfeature; link++) {
            weight = weight + w1[link+neuron*nfeature] * a0[link+batch*nfeature];
         }
         weight = weight + b1[neuron];
         z1[neuron+batch*nneuron1] = weight;
         a1[neuron+batch*nneuron1] = (weight > 0.0)? weight:0.0;
      }
   }
}

__global__
void first_layer_backward(int nbatch, float *z1, float *g_a0, float *g_z1, float *g_a1) {

   int batch = (blockIdx.x * blockDim.x + threadIdx.x);

   if ( batch < nbatch) {
      // calculate gradient of z1
      for (int neuron = 0; neuron < nneuron1; neuron++) {
         g_z1[neuron + batch * nneuron1] = (z1[neuron + batch * nneuron1] > 0.0)? g_a1[neuron + batch * nneuron1]: 0.0;
      }

      // calculate gradient of a0
      for (int neuron = 0; neuron < nfeature; neuron++) {
         g_a0[neuron + batch * nfeature] = 0.0;
      }
      for (int neuron = 0; neuron < nneuron1; neuron++) {
         for (int link = 0; link < nfeature; link++) {
            g_a0[link + batch * nfeature] += g_z1[neuron + batch * nneuron1] * w1[link + neuron * nfeature];
         }
      }
   }
}
//
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
// The second layer kernel for mlses NN
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
__global__
void second_layer(int nbatch, float *a1, float *a2, float *z2) {

   int batch = (blockIdx.x * blockDim.x + threadIdx.x);

   if ( batch < nbatch) {
      for (int neuron = 0; neuron < nneuron2; neuron++) {
         float weight = 0.0;
         for (int link = 0; link < nneuron1; link++) {
            weight = weight + w2[link+neuron*nneuron1] * a1[link+batch*nneuron1];
         }
         weight = weight + b2[neuron];
         z2[neuron+batch*nneuron2] = weight;
         a2[neuron+batch*nneuron2] = (weight > 0.0)? weight:0.0;
      }
   }
}

__global__
void second_layer_backward(int nbatch, float *z2, float *g_a1, float *g_z2, float *g_a2) {

   int batch = (blockIdx.x * blockDim.x + threadIdx.x);

   if ( batch < nbatch) {
      // calculate gradient of z2
      for (int neuron = 0; neuron < nneuron2; neuron++) {
         g_z2[neuron + batch * nneuron2] = (z2[neuron + batch * nneuron2] > 0.0)? g_a2[neuron + batch * nneuron2]: 0.0;
      }

      // calculate gradient of a1
      for (int neuron = 0; neuron < nneuron1; neuron++) {
         g_a1[neuron + batch * nneuron1] = 0.0;
      }
      for (int neuron = 0; neuron < nneuron2; neuron++) {
         for (int link = 0; link < nneuron1; link++) {
            g_a1[link + batch * nneuron1] += g_z2[neuron + batch * nneuron2] * w2[link + neuron * nneuron1];
         }
      }
   }
}
//
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
// The third layer kernel for mlses NN
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
__global__
void third_layer(int nbatch, float *a2, float *pr, float *zp) {

   int batch = (blockIdx.x * blockDim.x + threadIdx.x);

   if ( batch < nbatch) {
      float weight = 0.0;
      for (int link = 0; link < nneuron2; link++) {
         weight = weight + w3[link] * a2[link+batch*nneuron2];
      }
      weight = weight + b3;
      zp[batch] = weight;

      // modify the last layer output with ReLU activation function and do translation
      pr[batch] = (weight > 0.0)? weight - 1.5: -1.5;
   }
}

__global__
void third_layer_backward(int nbatch, float *zp, float *g_a2, float *g_zp) {

   int batch = (blockIdx.x * blockDim.x + threadIdx.x);

   if ( batch < nbatch) {
      // modify the last layer output with ReLU activation function and do translation
      g_zp[batch] = (zp[batch] > 0.0)? 1.0: 0.0;
      for (int link = 0; link < nneuron2; link++) {
         g_a2[link + batch * nneuron2] = w3[link] * g_zp[batch];
      }
   }
}
