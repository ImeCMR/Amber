   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a0 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&z1, sizeof(float)*nbatch*nneuron1);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for z1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&a1, sizeof(float)*nbatch*nneuron1);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&z2, sizeof(float)*nbatch*nneuron2);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for z2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&a2, sizeof(float)*nbatch*nneuron2);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&zp, sizeof(float)*nbatch         );
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for zp with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&pr, sizeof(float)*nbatch         );
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for pr with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&g_a0, sizeof(float)*nbatch*nfeature);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for g_a0 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&g_z1, sizeof(float)*nbatch*nneuron1);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for g_z1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&g_a1, sizeof(float)*nbatch*nneuron1);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for g_a1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&g_z2, sizeof(float)*nbatch*nneuron2);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for g_z2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&g_a2, sizeof(float)*nbatch*nneuron2);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for g_a2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&g_zp, sizeof(float)*nbatch         );
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for g_zp with error \"%s\".\n", cudaGetErrorString(cudaErr));

   cudaErr = cudaMemcpy(a0, h_a0, sizeof(float)*nbatch*nfeature, cudaMemcpyHostToDevice);
   if ( cudaErr != cudaSuccess )
      printf("Host to Device MemCpy failed for a0 with error \"%s\".\n", cudaGetErrorString(cudaErr));

   int threadsPerBlock = 512;
   int blocks = (nbatch - 1) / threadsPerBlock + 1;

   // first layer
   first_layer <<<blocks, threadsPerBlock>>>(nbatch, a0, a1, z1);

   // second layer
   second_layer<<<blocks, threadsPerBlock>>>(nbatch, a1, a2, z2);

   // third layer
   third_layer <<<blocks, threadsPerBlock>>>(nbatch, a2, pr, zp);

   // third layer backward
   third_layer_backward <<<blocks, threadsPerBlock>>>(nbatch, zp, g_a2, g_zp);

   // second layer backward
   second_layer_backward<<<blocks, threadsPerBlock>>>(nbatch, z2, g_a1, g_z2, g_a2);

   //first layer backward
   first_layer_backward <<<blocks, threadsPerBlock>>>(nbatch, z1, g_a0, g_z1, g_a1);

   cudaErr = cudaMemcpy(h_grad, g_a0, sizeof(float)*nbatch*nfeature, cudaMemcpyDeviceToHost);
   if ( cudaErr != cudaSuccess )
      printf("Device to Host MemCpy failed for grad with error \"%s\".\n", cudaGetErrorString(cudaErr));

}


//
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
// The cuda implementation of mlses wrapper
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
extern "C"
void mlses_predict_cuda_(int *inbatch, float *h_a0, float *h_pred, float *h_grad, int *do_backward)
{
   int nbatch = *inbatch;
   bool enable_gradient = *do_backward == 0? false: true;

   cudaError_t cudaErr;
   float *a0;
   float *z1;
   float *a1;
   float *z2;
   float *a2;
   float *zp;
   float *pr;
   cudaErr = cudaMalloc(&a0, sizeof(float)*nbatch*nfeature);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a0 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&z1, sizeof(float)*nbatch*nneuron1);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for z1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&a1, sizeof(float)*nbatch*nneuron1);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&z2, sizeof(float)*nbatch*nneuron2);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for z2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&a2, sizeof(float)*nbatch*nneuron2);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&zp, sizeof(float)*nbatch         );
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for zp with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&pr, sizeof(float)*nbatch         );
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for pr with error \"%s\".\n", cudaGetErrorString(cudaErr));

   // backward pointers
   float *g_a0;
   float *g_z1;
   float *g_a1;
   float *g_z2;
   float *g_a2;
   float *g_zp;
   if (enable_gradient) {
      cudaErr = cudaMalloc(&g_a0, sizeof(float)*nbatch*nfeature);
      if ( cudaErr != cudaSuccess )
         printf("Device malloc failed for g_a0 with error \"%s\".\n", cudaGetErrorString(cudaErr));
      cudaErr = cudaMalloc(&g_z1, sizeof(float)*nbatch*nneuron1);
      if ( cudaErr != cudaSuccess )
         printf("Device malloc failed for g_z1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
      cudaErr = cudaMalloc(&g_a1, sizeof(float)*nbatch*nneuron1);
      if ( cudaErr != cudaSuccess )
         printf("Device malloc failed for g_a1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
      cudaErr = cudaMalloc(&g_z2, sizeof(float)*nbatch*nneuron2);
      if ( cudaErr != cudaSuccess )
         printf("Device malloc failed for g_z2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
      cudaErr = cudaMalloc(&g_a2, sizeof(float)*nbatch*nneuron2);
      if ( cudaErr != cudaSuccess )
         printf("Device malloc failed for g_a2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
      cudaErr = cudaMalloc(&g_zp, sizeof(float)*nbatch         );
      if ( cudaErr != cudaSuccess )
         printf("Device malloc failed for g_zp with error \"%s\".\n", cudaGetErrorString(cudaErr));
   }

   // copy input features
   cudaErr = cudaMemcpy(a0, h_a0, sizeof(float)*nbatch*nfeature, cudaMemcpyHostToDevice);
   if ( cudaErr != cudaSuccess )
      printf("Host to Device MemCpy failed for a0 with error \"%s\".\n", cudaGetErrorString(cudaErr));

   int threadsPerBlock = 512;
   int blocks = (nbatch - 1) / threadsPerBlock + 1;

   // first layer
   first_layer <<<blocks, threadsPerBlock>>>(nbatch, a0, a1, z1);

   // second layer
   second_layer<<<blocks, threadsPerBlock>>>(nbatch, a1, a2, z2);

   // third layer
   third_layer <<<blocks, threadsPerBlock>>>(nbatch, a2, pr, zp);

   cudaErr = cudaMemcpy(h_pred, pr, sizeof(float)*nbatch, cudaMemcpyDeviceToHost);
   if ( cudaErr != cudaSuccess )
      printf("Device to Host MemCpy failed for pr with error \"%s\".\n", cudaGetErrorString(cudaErr));

   // calculate gradients if needed
   if (enable_gradient) {
      // third layer backward
      third_layer_backward <<<blocks, threadsPerBlock>>>(nbatch, zp, g_a2, g_zp);

      // second layer backward
      second_layer_backward<<<blocks, threadsPerBlock>>>(nbatch, z2, g_a1, g_z2, g_a2);

      //first layer backward
      first_layer_backward <<<blocks, threadsPerBlock>>>(nbatch, z1, g_a0, g_z1, g_a1);

      cudaErr = cudaMemcpy(h_grad, g_a0, sizeof(float)*nbatch*nfeature, cudaMemcpyDeviceToHost);
      if ( cudaErr != cudaSuccess )
         printf("Device to Host MemCpy failed for grad with error \"%s\".\n", cudaGetErrorString(cudaErr));
   }

}
