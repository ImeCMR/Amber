//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
// The cuda implementation of mlses function
//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
extern "C"
void mlses_function_cuda_(int *inbatch, float *h_a0, float *h_pred)
{
   int nbatch = *inbatch;

   cudaError_t cudaErr;
   float *a0;
   float *z1;
   float *a1;
   float *z2;
   float *a2;
   float *zp;
   float *pr;
   cudaErr = cudaMalloc(&a0, sizeof(float)*nbatch*nfeature);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a0 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&z1, sizeof(float)*nbatch*nneuron1);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for z1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&a1, sizeof(float)*nbatch*nneuron1);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a1 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&z2, sizeof(float)*nbatch*nneuron2);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for z2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&a2, sizeof(float)*nbatch*nneuron2);
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for a2 with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&zp, sizeof(float)*nbatch         );
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for zp with error \"%s\".\n", cudaGetErrorString(cudaErr));
   cudaErr = cudaMalloc(&pr, sizeof(float)*nbatch         );
   if ( cudaErr != cudaSuccess )
      printf("Device malloc failed for pr with error \"%s\".\n", cudaGetErrorString(cudaErr));

   cudaErr = cudaMemcpy(a0, h_a0, sizeof(float)*nbatch*nfeature, cudaMemcpyHostToDevice);
   if ( cudaErr != cudaSuccess )
      printf("Host to Device MemCpy failed for a0 with error \"%s\".\n", cudaGetErrorString(cudaErr));

   int threadsPerBlock = 512;
   int blocks = (nbatch - 1) / threadsPerBlock + 1;

   // first layer
   first_layer <<<blocks, threadsPerBlock>>>(nbatch, a0, a1, z1);

   // second layer
   second_layer<<<blocks, threadsPerBlock>>>(nbatch, a1, a2, z2);

   // third layer
   third_layer <<<blocks, threadsPerBlock>>>(nbatch, a2, pr, zp);

   cudaErr = cudaMemcpy(h_pred, pr, sizeof(float)*nbatch, cudaMemcpyDeviceToHost);
   if ( cudaErr != cudaSuccess )
      printf("Device to Host MemCpy failed for pr with error \"%s\".\n", cudaGetErrorString(cudaErr));

}

