class Restraint(object):
    def __init__(self,line):
        import re
        import numpy as np
        
        self.line = line
        self.angle = False
        self.dihed = False
        self.r12 = False
        self.iat = []
        self.rstwt = []
        self.r1 = -1000.
        self.r2 = 0
        self.r3 = 0
        self.r4 = 1000.
        self.rk2 = 0
        self.rk3 = 0

        line = line.replace(","," ")
        #print(line)
        cols = line.split()
        cols.remove("&rst")
        if "/" in cols:
            cols.remove("/")
        if "&end" in cols:
            cols.remove("&end")
        line = " ".join(cols)
        
        #print(line)
        
        keyvals = re.findall(r"(.*?)=([^=]+)",line)
        for i in range(len(keyvals)):
            k,v = keyvals[i]
            keyvals[i] = (k.strip(),v.strip())
        for i in range(len(keyvals)):
            k,v = keyvals[i]
            if len(k) == 0:
                pk,pv = keyvals[i-1]
                cols = pv.split()
                k = cols.pop()
                pv = " ".join(cols)
                keyvals[i-1] = pk,pv
                keyvals[i] = k,v
                
        #print(keyvals)
        
        for k,v in keyvals:
            if k == "iat":
                self.iat = [ int(x) for x in v.split() ]
                self.angle=False
                if len(self.iat) == 3:
                    self.angle = True
                elif len(self.iat) == 4:
                    if "rstwt" in [ k for k,v in keyvals ]:
                        self.angle = False
                        self.r12 = True
                    else:
                        self.angle = True
                        self.dihed = True
            elif k == "rk2":
                self.rk2 = float(v)
            elif k == "rk3":
                self.rk3 = float(v)
            elif k == "r1":
                self.r1 = float(v)
            elif k == "r2":
                self.r2 = float(v)
            elif k == "r3":
                self.r3 = float(v)
            elif k == "r4":
                self.r4 = float(v)
            elif k == "rstwt":
                self.rstwt = [ float(x) for x in v.split() ]
        #if self.angle:
            #f = (np.pi/180.)**2
            #self.rk2 *= f
            #self.rk3 *= f
        
    def energy(self,r):
        e = 0
        if r < self.r1:
            h = self.rk2 * (self.r1-self.r2)**2
            m = 2 * self.rk2 * (self.r1-self.r2)
            e = h + m*(r-self.r1)
        elif r < self.r2:
            e = self.rk2 * (r-self.r2)**2
        elif r < self.r3:
            e = 0
        elif r < self.r4:
            e = self.rk3 * (r-self.r3)**2
        else:
            h = self.rk3 * (self.r4-self.r3)**2
            m = 2 * self.rk3 * (self.r4-self.r3)
            e = h + m*(r-self.r4)
        return e

    def get_rxncrd(self,crds):
        import parmed.geometry
        import numpy as np
        
        if len(self.iat) == 2:
            a = crds[ self.iat[0] - 1 ]
            b = crds[ self.iat[1] - 1 ]
            x = np.sqrt( parmed.geometry.distance2(a,b) )
        elif self.dihed:
            a = crds[ self.iat[0] - 1 ]
            b = crds[ self.iat[1] - 1 ]
            c = crds[ self.iat[2] - 1 ]
            d = crds[ self.iat[3] - 1 ]
            x = parmed.geometry.dihedral(a,b,c,d) * np.pi / 180.
        elif self.angle:
            a = crds[ self.iat[0] - 1 ]
            b = crds[ self.iat[1] - 1 ]
            c = crds[ self.iat[2] - 1 ]
            x = parmed.geometry.angle(a,b,c) * np.pi / 180.
        elif len(self.iat) == 4:
            a = crds[ self.iat[0] - 1 ]
            b = crds[ self.iat[1] - 1 ]
            c = crds[ self.iat[2] - 1 ]
            d = crds[ self.iat[3] - 1 ]
            r1 = np.sqrt( parmed.geometry.distance2(a,b) )
            r2 = np.sqrt( parmed.geometry.distance2(c,d) )
            x = self.rstwt[0] * r1 + self.rstwt[1] * r2
        return x


    
    def get_umbrella(self):
        import numpy as np
        
        k = max(self.rk2,self.rk3)
        kj_per_kcal = 4.184
        nm_per_A = 0.1
        k = k * kj_per_kcal
        if not self.angle:
            k = k / ( nm_per_A * nm_per_A )
        k = k * 2.

        eq = 0.5 * ( self.r2 + self.r3 )
        if self.angle:
            eq = eq * np.pi / 180.
        else:
            eq = eq * nm_per_A
            
        return k,eq, self.rk2 == self.rk3, self.r2 == self.r3
    

def readdisang(fname):
    fh=open(fname,"r")
    restraints = []

    rline=""
    for line in fh:
        line = line.split("!")[0].strip()
        if "&rst" in line:
            rline=line
            if not ( "/" in line or "&end" in line ):
                for line in fh:
                    line = line.split("!")[0].strip()
                    if "/" in line or "&end" in line:
                        rline += " " + line
                        break
                    else:
                        rline += " " + line
            rline = rline.replace("/"," /")
            #print(rline)
            restraints.append( Restraint(rline) )
            rline=""
    fh.close()
    return restraints


class Disang(object):
    def __init__(self,fname):
        self.restraints = readdisang(fname)

    def energy(self,rcrds):
        e = 0
        n = len(rcrds)
        if n != len(self.restraints):
            raise Exception("len(rcrds) != len(self.restraints) (%i,%i)"%(n,len(self.restraints)))
        for i in range(n):
            e += self.restraints[i].energy(rcrds[i])
        return e





